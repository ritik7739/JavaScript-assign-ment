//Arithmetic Operator
let a=10;
let b=5;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a**b);

//unary operator
console.log(a++); //10
console.log(++a);  //12

//comparison operator  (  >, >=, <, <=, ==, )

let age=18;

console.log(age>18);   //false
console.log(age>=18);   //true
console.log(age<18);    //false
console.log(age<=18);   //true
console.log(age==age);  //true
console.log(age!=age);  //false