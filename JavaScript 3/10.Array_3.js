//push,pop,unshift,shift

let cars=["audi","Bmw","swift","Maruti"];

//push("toyota")------>push add toyota at the END
cars.push("toyota");
console.log(cars);
cars.push("Ferrari");
console.log(cars);


//pop() -----------> pop Remove the element from END
cars.pop();
console.log(cars);
cars.pop();
console.log(cars);

//unshift("toyota") -------------> unshift() Add the element form the start

cars.unshift("toyota");
console.log(cars);
cars.unshift("ferari");
console.log(cars);

//shift() --------------------> shift is used to remove element form the start

cars.shift();
console.log(cars);
cars.shift();
console.log(cars);



